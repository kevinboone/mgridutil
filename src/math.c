#include <stdlib.h>
#include <math.h>
#include "defs.h" 


/*======================================================================
 * sqr
 * square of x
 * =====================================================================*/
double sqr (double x) 
  {
  return x * x;
  }


/*======================================================================
 * sin2 
 * sine square of x
 * =====================================================================*/
double sin2 (double x) 
  {
  return sqr (sin (x));
  }


/*======================================================================
 * torad 
 * Convert degrees to radians 
 * =====================================================================*/
double torad (double x)
  {
  return x * 2 * M_PI / 360.0;
  }


/*======================================================================
 * todeg
 * Convert radians to degrees
 * =====================================================================*/
double todeg (double x)
  {
  return x * 360 / 2 / M_PI;
  }


/*======================================================================
 *   hav 
 *   haversine function, sin2(x/2) 
 * =====================================================================*/
double hav (double x)
  {
  return sin2 (x/2);
  }

